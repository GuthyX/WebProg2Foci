<h2 class="text-left">Átigazolások</h2>
<div class="Hirek">
<p>Nagy reményekkel érkezett az AC Milan együttesébe 3 évvel ezelőtt Mateo Musacchio. Az olasz kaland nem jött be annyira a számára, így kész visszatérni a La Ligába.



2017 nyarán hagyta el a Villarreal együttesét a Milan kedvéért Mateo Musacchio, 18 millió euró fejében. Az argentin védő 3 szezon alatt összesen 73 mérkőzésen ölthette magára a piros-feketék mezét.

A legfrissebb hírek szerint a 29 esztendős futballista készen áll arra, hogy elhagyja Milánót és visszatérjen Spanyolországba. A Calciomercato értesülései alapján kérőknek sincs híján a játékos, több klub közül is válogathat. A 3 fő érdeklődőként az oldal a Real Betist, a Sevillát és a Valencia együttesét nevezte meg. A portál szerint a védőt 10 millió euró körüli összegért mozdítható is.

A Milan számára is jól jöhetnek az érdeklődők, mivel a játékos szerződéséből már csak egy év van hátra. Ha ezen a nyáron nem adják el, akkor olcsóbban vagy szabadügynökként távozna a későbbiekben.</p>
<p>Olasz sajtóértesülések szerint a koronavírus-járvány miatti kényszerszünet okozta gazdasági válság következtében az AS Roma eladni kényszerülhet legnagyobb tehetségét…

A Leggo információi értelmében az anyagi veszteségek mérséklése érdekében az AS Roma fájdalmas döntést hozhat meg, és mégis eladhatja legnagyobb tehetségét, a jelentős nemzetközi érdeklődésnek örvendő Nicolo Zaniolót.

A fővárosiak 20 esztendős tehetsége az elmúlt szezonban robbant be az AS Roma csapatába, nagyszerű technikai megoldásai és hat találata révén hamar felhívta magára a nagy csapatok figyelmét és tavaly a felnőtt válogatottbeli debütáláson is áteshetett. Az idei szezonban súlyos sérüléséig 23 tétmérkőzésen lépett pályára, melyeken hat gólt és két gólpasszt regisztrált.

A forrás szerint a Juventus lehet a legnagyobb favorit Zaniolo megszerzésére, de az angol bajnokságból (Premier League) a Manchester United és a Tottenham egyaránt harcban áll kegyeiért.</p></div>
<h2 class="text-left">Botrányok</h2>
<h3 class="text-left">Ödegaard apja megszedte magát a Realnál:</h3>
<div class="Hirek">
<p>A Football Leaks héten kiadott második kiadásában a német Der Spiegel két munkatársa több, labdarúgáshoz kapcsolódó pénzügyi titok között azt is leleplezte, milyen jól járt a norvég Ödegaard család, amikor a tinédzser Martin 2015 januárjában aláírt a Real Madridhoz.


Martin Odegaard 2015-ben írt alá a Real Madridhoz (Fotó: AFP)
 

HIRDETÉS
A Manchester City, a Barcelona, a Liverpool, az Arsenal, a Bayern München és a Manchester United is érdeklődött a norvég csodagyerek iránt, s 2015 elején Ödegaard úgy döntött, a Real Madridhoz ír alá. A Football Leaks most rávilágított, hogy a döntésben nem feltétlenül puszta klubszimpátia döntött.

Martin Ödegaard szerződtetésének napján a Real Madridhoz aláírt Hans Erik Ödegaard, a labdarúgó édesapja is, aki egy norvég kiscsapat segédedzőjéből lett a madridiak U11-es csapatának edzője. Ezt akkor sem titkolta el a Real, azt viszont igen, hogy Ödegaard apuka az állandó fizetése mellé kapott hárommillió euró aláírási pénzt is.


Ödegaard apja is jól járt a kettős szerződéssel 
Az ilyen jellegű ráhatásokat Angliában egyenesen tiltják (a Chelsea egykor bepróbálkozott Andreas Christensen édesapjának alkalmazásával, de a dán apja kezdő játékosmegfigyelőként ennek töredékét kapta), és nyilván Spanyolországban sem nézik jó szemmel. Egyik klub sem fizet hárommillió eurót az U11-es csapat edzőjének három és fél évre.

Sőt, a Football Leaks cikke a belső levelezésre hivatkozva arra is rámutat, hogy a klub vezetőségén belül is heves indulatokat váltott ki ez a lépés, hiszen a Real Madrid utánpótlásának ezen szintjén dolgozók átlagosan 12 ezer eurót kerestek évente. Még Zinédine Zidane is csak 600 ezret kapott évente a Castilla (azaz a felnőtt B-csapat) edzőjeként.

A norvég VG megkereste Ödegaard apukát, de a szakember, ahogy a Football Leaks összeállítást 70 millió, eddig titkos dokumentum felhasználásával publikáló Der Spiegel esetében, nem válaszolt a kérdéseire. A norvég lap csak annyit derített ki, hogy az edző a hazai adóhatóságokhoz 2015 óta nulla korona bevételt vallott be.

Hans Erik Ödegaard jelenleg a Strömsgodset U16-os csapatánál dolgozik, most 20 éves fia a Realtól kölcsönben a Real Sociedadnál játszik.</p></div>